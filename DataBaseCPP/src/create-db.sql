CREATE TABLE PROPOSAL
(
PID integer,
STATUS integer,

PRIMARY KEY(PID)
);

CREATE TABLE TARGET
(
TID integer,
PID integer,
EXP_TIME integer,
EL float,
AZ float,

PRIMARY KEY (TID, PID),
FOREIGN KEY (PID) REFERENCES PROPOSAL (PID)
);

CREATE TABLE IMAGE
(
TID integer,
PID integer,
IMGBIN BLOB,

PRIMARY KEY (TID, PID),
FOREIGN KEY (PID, TID) REFERENCES TARGET (PID, TID)
);

COMMIT;
